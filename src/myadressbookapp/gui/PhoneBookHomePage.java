    class MyTableModel extends DefaultTableModel {

        Vector header;
        Vector<Vector> data;

        public MyTableModel() {
            header = new Vector();
            header.add("Select");
            header.add("Id");
            header.add("Name");
            header.add("Phone No");
            header.add("Email");
            header.add("Address");
            header.add("Group Type");
            int uid = LoginForm.loggedInUser.getUserId();
            try {
                data = contactServiceImpl.getAllContacts(uid);
            } catch (Exception e) {
                e.printStackTrace();
            }
            setDataVector(data, header);

        }

        @Override
        public boolean isCellEditable(int row, int column) {
            if (column == 0) {
                return true;
            } else {
                return false;
            }
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (columnIndex == 0) {
                return Boolean.class;
            }
            return super.getColumnClass(columnIndex);

        }
    }
