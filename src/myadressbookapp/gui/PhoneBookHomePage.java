    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int rowCount = tblContacts.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                Boolean isChecked = (Boolean) myTableModel.getValueAt(i, 0);
                if (isChecked) {
                    Integer cid = (Integer) myTableModel.getValueAt(i, 1);
                    contactServiceImpl.deleteContact(cid);
                }
            }
            deleteAllRowsFromTable();
            fetchAllContacts();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Contact is not deleted. ??");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        //Check for Enter key : 10
        if (evt.getKeyCode() != 10) {
            return;
        }
        try {
            search();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtSearchKeyReleased

