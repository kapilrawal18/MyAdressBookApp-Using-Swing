    private void deleteAllRowsFromTable() {
        int rowCount = tblContacts.getRowCount();
        if (rowCount >= 1) {
            myTableModel.removeRow(rowCount - 1);
            deleteAllRowsFromTable(); //recursive call
        } else {
            return;
        }

    }

    private void fetchAllContacts() throws Exception {
        Vector<Vector> data = contactServiceImpl.getAllContacts(LoginForm.loggedInUser.getUserId());
        for (Vector row : data) {
            myTableModel.addRow(row);
        }
    }

    private void search() throws Exception {
        deleteAllRowsFromTable();
        String key = txtSearch.getText();
        Vector<Vector> data = contactServiceImpl.search(key, LoginForm.loggedInUser.getUserId());
        for (Vector row : data) {
            myTableModel.addRow(row);
        }
    }

